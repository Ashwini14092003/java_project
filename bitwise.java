class bitwise 
{
public static void main(String []args)
{
int p=7;
int s=5;
System.out.println("The value of p&s is:"+(p&s));
System.out.println("The value of p|s is:"+(p|s));
System.out.println("The value of p^s is:"+(p^s));
System.out.println("The value of ~p   is:"+(~p));
System.out.println("The value of ~s   is:"+(~s));
System.out.println("the value of p<<2 is:"+(p<<2));
System.out.println("The value of s<<2 is:"+(s<<2));
System.out.println("the value of p>>2 is:"+(p>>2));
}
} 